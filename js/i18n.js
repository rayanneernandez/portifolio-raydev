/* idioma atual (salvo no navegador) */
const LANG = (function () {
  try { return localStorage.getItem('lang') || 'pt'; } catch (e) { return 'pt'; }
})();

/* atalho: busca uma string no idioma atual, caindo pro PT */
function t(key) {
  return (I18N[LANG] && I18N[LANG][key]) || I18N.pt[key] || '';
}

/* aplica as traduções no HTML (PT é o texto original — não sobrescreve) */
function applyI18n() {
  if (LANG === 'pt') return;
  const dict = I18N[LANG];
  if (!dict) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.dataset.i18n;
    if (dict[k] != null) el.innerHTML = dict[k];
  });
}
